package day05_primitives_concatanation;

public class WatchInfo {
    public static void main(String[] args) {
        String brand = "Fosil";
        String color = "Black";
        double price = 1250.99;
        boolean waterResistant = true;
        boolean isSmart = true;
        char gender = 'U';

        System.out.println("Brand: " + "\n\t"+ brand);
        System.out.println("Color: " +"\n\t"+ color);
        System.out.println("Price: " + "\n\t$" + price);
        System.out.println("Water Resistant: " + "\n\t" + waterResistant);
        System.out.println("Smart Watch: " + "\n\t" + isSmart);
        System.out.println("Gender: " + "\n\t"+ gender);
    }
}
