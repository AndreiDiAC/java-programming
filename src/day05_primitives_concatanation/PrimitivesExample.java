package day05_primitives_concatanation;

public class PrimitivesExample {
    public static void main(String[] args) {
        // ******* WHOLE NUMBERS/ INTEGERS TYPES *****
        byte byteValue = 100;
        short shorValue = 32000;
        int intValue = 2_134_314_314;
        long longValue = 4215123753241L;

        // ****** DECIMAL NUMBERS/ FLOAT POINT TYPES ******
        float floatValue = 234.55F;
        double doubleValue = 2412497.5326483;

        // ******* CHARACTER TYPE ******
        char charValue = '%';

        // ****** BOOLEAN - true|false ******
        boolean booleanValue = true;


        System.out.println(byteValue);
        System.out.println(shorValue);
        System.out.println(intValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(charValue);
        System.out.println(booleanValue);
    }
}
